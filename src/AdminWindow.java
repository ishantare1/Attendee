    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id=jTextField1.getText();
        String pass=new String(jPasswordField1.getPassword());
        String newPass=new String(jPasswordField2.getPassword());
        String cNewPass=new String(jPasswordField3.getPassword());

        SessionFactory sf= new Configuration().configure().buildSessionFactory();
        Session session=sf.openSession();
        Transaction tx=session.beginTransaction();
        Criteria crit = session.createCriteria(User.class);
        crit.add(Restrictions.and (Restrictions.eq("email",id),Restrictions.eq("password",pass)));
        List<User>data =crit.list();
        if(data.isEmpty())
        {
            jLabel5.setText("incorrect old password");
            jPanel3.setVisible(true);
        }
        else
        {
            if(newPass.equals(cNewPass))
            {
                //          new AdminWindow().setVisible(true);
                User user=(User)session.get(User.class,id);
                user.setPassword(newPass);
                session.update(user);
                tx.commit();
                new AdminWindow().setVisible(true);
                dispose();

            }

            else{
                jLabel5.setText("check");
                jPanel3.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

