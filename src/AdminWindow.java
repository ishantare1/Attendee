public void loadData()
{
SessionFactory sf=new Configuration().configure().buildSessionFactory();
Session session=sf.openSession();
Transaction tx=session.beginTransaction();
Criteria crit= session.createCriteria(Student.class);
List<Student> list=crit.list();
dtm1 =(DefaultTableModel)jTable1.getModel();
for(Student ref: list)
{
Object records[]={ref.getStudentId(),ref.getStudentName(),ref.getGender(),ref.getContact(),
    ref.getEmail(),ref.getAddress() ,ref.getCollegeName()};
 
dtm1.addRow(records);
jComboBox4.addItem(""+ref.getStudentId());
}
}

